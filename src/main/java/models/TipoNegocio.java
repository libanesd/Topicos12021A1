package models;


public class TipoNegocio {
	

	private Boolean disponibilidade;
	private ModalidadeCarros modalidadeCarros;
	
	
	public Boolean getDisponibilidade() {
		return disponibilidade;
	}
	public void setDisponibilidade(Boolean disponibilidade) {
		this.disponibilidade = disponibilidade;
	}
	public ModalidadeCarros getModalidadeCarros() {
		return modalidadeCarros;
	}
	public void setModalidadeCarros(ModalidadeCarros modalidadeCarros) {
		this.modalidadeCarros = modalidadeCarros;
	}
	
}
